// Faça  um programa  na  Linguagem Java que  calcule  a  área  da circunferência.
// Fórmula => (A = π r²)
public class ex06 {
	public static void main(String[] args) {
       double r = 5;
       double pi = 3.14;
       double area = pi * (r * r);

       System.out.println("A área da circunferência é : "+ area);
	}
}